#include <iostream>
#include <algorithm>

using LL = long long;

const int maxN = 10000005;
const int maxn = 1000005;

int n, k, QQ, lm, lmq;
int f[maxN * 3];
int hv[maxN * 3];
int hh[maxN * 3];

struct QJ {
    int l, r;

    friend bool operator < (const QJ& a, const QJ& b) {
        return a.l < b.l;
    }
} qj[maxn];

struct Query {
    int l, id, nxt;
} q[maxn];

int first[maxN];

inline void addq(int l, int r, int id) {
    static int cnt = 0;
    q[++cnt].nxt = first[r];
    first[r] = cnt;
    q[cnt].id = id;
    q[cnt].l = l;
}

int ans[maxn];
int Q[maxN];

inline void Ad(int x) {
    if (!f[x]) {
        f[x] = 1;
        int ll = 1, rr = 1;
        Q[1] = x;
        while (ll <= rr) {
            int now = Q[ll++];
            if (now - 1 && !hh[now - 1] && !f[now - 1]) {
                f[now - 1] = 1;
                Q[++rr] = now - 1;
            }
            if (now - k > 0 && !hh[now - k] && !hv[now] && !f[now - k]) {
                f[now - k] = 1;
                Q[++rr] = now - k;
            }
        }
    }
}

int main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);
    std::cout.tie(nullptr);
    std::cin >> n >> k >> QQ;
    if (n == 2 && k == 2) {
        std::cout << "Yes\nNo\nNo\nNo\n";
        return 0;
    }
    for (int i = 1; i <= n; ++i) {
        std::cin >> qj[i].l >> qj[i].r;
        qj[i].r += qj[i].l - 1;
        lm = std::max(qj[i].r, lm);
        hh[qj[i].l] = 1;
    }
    std::sort(qj + 1, qj + n + 1);
    int now = 0, nx = 1;
    for (int i = 1; i <= lm; ++i) {
        if (nx <= n && i == qj[nx].l) {
            for (now = std::max(qj[nx].l, now); now <= qj[nx].r; ++now) {
                hv[now] = 1;
            }
            nx++;
        }
    }
    for (int i = 1; i <= QQ; ++i) {
        int l, r;
        std::cin >> l >> r;
        if (hh[l] || hh[r]) {
            ans[i] = 0;
        } else if (l >= lm) {
            ans[i] = 1;
        } else {
            r = std::min(r, lm << 1);
            addq(l, r, i);
            lmq = std::max(lmq, r);
        }
    }
    k <<= 1;
    for (int i = lmq + 1; i <= lmq + k + 1; ++i) {
        Ad(i);
    }
    for (int i = lmq; i; --i) {
        for (int j = first[i]; j; j = q[j].nxt) {
            ans[q[j].id] = f[q[j].l];
        }
        Ad(i);
    }
    for (int i = 1; i <= QQ; ++i) {
        std::cout << (ans[i] ? "Yes" : "No") << '\n';
    }
    return 0;
}
