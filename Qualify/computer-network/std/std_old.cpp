#include <cstdio>
#include <cctype>
#include <algorithm>

using std::sort;

typedef long long LL;

inline char gc() {
	static const int L = 233333;
	static char sxd[L], *sss = sxd, *ttt = sxd;
	if (sss == ttt) {
		ttt = (sss = sxd) + fread(sxd, 1, L, stdin);
		if (sss == ttt) {
			return EOF;
		}
	}
	return *sss++;
}

#define dd c = gc()
template <class T>
inline bool read(T& x) {
	bool flg = false;
	char dd;
	x = 0;
	for (; !isdigit(c); dd) {
		if (c == '-') {
			flg = true;
		} else if(c == EOF) {
			return false;
		}
	}
	for (; isdigit(c); dd) {
		x = (x << 1) + (x << 3) + (c ^ 48);
	}
	if (flg) {
		x = -x;
	}
	return true;
}
#undef dd

const int maxN = 10000005;
const int maxn = 1000005;

int n, k, QQ, lm, lmq;
int f[maxN * 3];
int hv[maxN * 3];
int hh[maxN * 3];

inline int Max(const int x, const int y) {
	return x < y ? y : x;
}

inline int Min(const int x, const int y) {
	return x < y ? x : y;
}

struct QJ {
	int l, r;

	friend bool operator < (const QJ& a, const QJ& b) {
		return a.l < b.l;
	}
} qj[maxn];

struct Query {
	int l, id, nxt;
} q[maxn];

int first[maxN];

inline void addq(int l, int r, int id) {
	static int cnt = 0;
	q[++cnt].nxt = first[r];
	first[r] = cnt;
	q[cnt].id = id;
	q[cnt].l = l;
}

int ans[maxn];
int Q[maxN];

inline void Ad(int x) {
	if (!f[x]) {
		f[x] = 1;
		int ll = 1, rr = 1;
		Q[1] = x;
		while (ll <= rr) {
			int now = Q[ll++];
			if (now - 1 && !hh[now - 1] && !f[now - 1]) {
				f[now - 1] = 1;
				Q[++rr] = now - 1;
			}
			if (now - k > 0 && !hh[now - k] && !hv[now] && !f[now - k]) {
				f[now - k] = 1;
				Q[++rr] = now - k;
			}
		}
	}
}

int main() {
	read(n), read(k), read(QQ);
	for (int i = 1; i <= n; ++i) {
		read(qj[i].l), read(qj[i].r);
		qj[i].r += qj[i].l - 1;
		lm = Max(qj[i].r, lm);
		hh[qj[i].l] = 1;
	}
	sort(qj + 1, qj + n + 1);
	int now = 0, nx = 1;
	for (int i = 1; i <= lm; ++i) {
		if (nx <= n && i == qj[nx].l) {
			for (now = Max(qj[nx].l, now); now <= qj[nx].r; ++now) {
				hv[now] = 1;
			}
			nx++;
		}
	}
	for (int i = 1; i <= QQ; ++i) {
		int l, r;
		read(l), read(r);
		if (hh[l] || hh[r]) {
			ans[i] = 0;
		} else if (l >= lm) {
			ans[i] = 1;
		} else {
			r = Min(r, lm << 1);
			addq(l, r, i);
			lmq = Max(lmq, r);
		}
	}
	k <<= 1;
	for (int i = lmq + 1; i <= lmq + k + 1; ++i) {
		Ad(i);
	}
	for (int i = lmq; i; --i) {
		for (int j = first[i]; j; j = q[j].nxt) {
			ans[q[j].id] = f[q[j].l];
		}
		Ad(i);
	}
	for (int i = 1; i <= QQ; ++i) {
		puts(ans[i] ? "Yes" : "No");
	}
	return 0;
}
